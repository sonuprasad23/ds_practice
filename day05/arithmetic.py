# day05/arithmetic.py
a = 15.0
b = 4

print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")   # True division
print(f"{a} // {b} = {a // b}") # Floor division
print(f"{a} % {b} = {a % b}")   # Modulo
print(f"{a} ** {b} = {a ** b}") # Exponentiation